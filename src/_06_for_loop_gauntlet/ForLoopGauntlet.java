package _06_for_loop_gauntlet;

public class ForLoopGauntlet {

	public static void main(String[] args) {
		
		for (int i = 0; i < 101; i++) {
			
				if(i%2==0) {
					System.out.println(i);
				
				}
				
			}
			System.out.println();
			
		}
	}
	






